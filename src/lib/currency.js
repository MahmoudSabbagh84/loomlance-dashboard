export function formatCurrency(amount, currency, locale) {
  const loc = locale || (typeof navigator !== 'undefined' ? navigator.language : 'en-US')
  return new Intl.NumberFormat(loc, {
    style: 'currency',
    currency,
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(amount)
}
